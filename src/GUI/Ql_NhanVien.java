        txt_Email.setText(email);
        txt_DC.setText(diachi);
        txt_MaNV.setText(ma);
        txt_TenNV.setText(hoten);
        txt_SDT.setText(sdt);
        txt_NS.setText(nsinh);
        txt_Anh.setText(hinh);
        if(!txt_Anh.getText().equals(""))
        {
            ImageIcon imgicon = new ImageIcon(new ImageIcon(txt_Anh.getText()).getImage().getScaledInstance(90, 110, Image.SCALE_SMOOTH));
            lbl_img.setIcon(imgicon);
        }
        txt_Quyen.setText(quyen);
