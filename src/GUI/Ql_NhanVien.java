    public void laydulieuNV(ArrayList<NhanVien> dsnv){
        tblData.removeAllElements();
        for(NhanVien nv : dsnv){
            Vector v = new Vector();
            v.add(nv.getMa().trim()); v.add(nv.getTen().trim()); v.add(nv.getNgaysinhString().trim());
            v.add(nv.getGioitinhString().trim()); v.add(nv.getDiachiString().trim()); v.add(nv.getSdt().trim()); 
            v.add(nv.getEmail().trim()); v.add(nv.getTaikhoanString().trim()); v.add(nv.getMatkhauString().trim()); 
            v.add(nv.getQuyen().trim()); 
            if(nv.getHinhString() == null)
                 v.add("Rỗng");
            else
                v.add(nv.getHinhString());
            tblData.add(v);
        }
        tbl_NhanVien.setModel(new DefaultTableModel(tblData, tblTitle));
    }

