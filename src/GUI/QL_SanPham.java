    private void btn_ThemSPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ThemSPActionPerformed
        // TODO add your handling code here:
        if(JOptionPane.showConfirmDialog(this, "Bạn có chắc muốn thêm không?", "Thông báo", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION){
                String ma = txt_MSP.getText().trim();
                String ten = txt_TSP.getText().trim();
                int gia = Integer.parseInt(txt_Gia.getText().trim());
                String dvt = txt_DVT.getText().trim();
                String maLSP = cbo_LoaiSP.getSelectedItem().toString();
                String sql = "insert into SanPham(MaSP, TenSP, GiaTien, DVT, MaLSP) values ('"+ma+"', N'"+ten+"', "+gia+", N'"+dvt+"', '"+maLSP+"')";
                int kq=SanPhamDAO.ThemXoaSua(sql);
                if(kq!=-1)
                {
                    dssp = SanPhamDAO.getDSSP();
                    laydulieuSP(dssp);
                    JOptionPane.showMessageDialog(this, "Thêm thành công !", "Thông báo",1);
                }
                else
                     JOptionPane.showMessageDialog(this, "Mã SP này đã tồn tại, không thể thêm", "Thông báo", 2);
                    
            }
    }//GEN-LAST:event_btn_ThemSPActionPerformed

    private void btn_XoaSPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_XoaSPActionPerformed
        // TODO add your handling code here:
        if(tbl_SP.getSelectedRowCount()==1)
        {
            if(JOptionPane.showConfirmDialog(this, "Bạn có chắc muốn xóa không?", "Thông báo", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION){
                String ma = txt_MSP.getText().trim();
                String sql = "delete from SanPham where MaSP = '"+ma+"'";
                if(SanPhamDAO.ThemXoaSua(sql)!=-1)
                {
                     dssp = SanPhamDAO.getDSSP();
                    laydulieuSP(dssp);
                    JOptionPane.showMessageDialog(this, "Xóa thành công !", "Thông báo", 1);
                }
                else
                    JOptionPane.showMessageDialog(this, "Dữ liệu này đang được sử dụng, không thể xóa", "Thông báo", 2);
                    
            }
        }else{
            if(tbl_SP.getRowCount()==0)
                JOptionPane.showMessageDialog(this, "Bảng trống","Thông báo",2);
            else
                JOptionPane.showMessageDialog(this, "Chưa chọn dòng cần xóa","Thông báo",2);
        }
    }//GEN-LAST:event_btn_XoaSPActionPerformed

