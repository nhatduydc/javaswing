    public void laydulieuSP(ArrayList<SanPham> dssp){
        tblDataSP.removeAllElements();
        for(SanPham nv : dssp){
            Vector v = new Vector();
            v.add(nv.getMasp()); v.add(nv.getTensp()); v.add(nv.getGia());
            v.add(nv.getDvtS()); v.add(nv.getMalsp()); 
            tblDataSP.add(v);
        }
        tbl_SP.setModel(new DefaultTableModel(tblDataSP, tblTitleSP));
    }
    public void laydulieuLSP(ArrayList<LoaiSP> dslsp){
        tblDataLSP.removeAllElements();
        for(LoaiSP lsp : dslsp){
            Vector v = new Vector();
            v.add(lsp.getMaloai()); v.add(lsp.getTenloai());
            tblDataLSP.add(v);
        }
        tbl_LoaiSP.setModel(new DefaultTableModel(tblDataLSP, tblTitleLSP));
    }
