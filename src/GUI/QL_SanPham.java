    private void btn_ThemLSPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ThemLSPActionPerformed
        // TODO add your handling code here:
        if(JOptionPane.showConfirmDialog(this, "Bạn có chắc muốn thêm không?", "Thông báo", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION){
                String ma = txt_MaLSP.getText().trim();
                String ten = txt_TenLSP.getText().trim();
                String sql = "insert into LoaiSanPham(MaLSP, TenLSP) values ('"+ma+"', N'"+ten+"')";
                int kq=LoaiSPDAO.ThemXoaSua(sql);
                if(kq!=-1)
                {
                    dslsp = LoaiSPDAO.getDSLSP();
                    laydulieuLSP(dslsp);
                    JOptionPane.showMessageDialog(this, "Thêm thành công !", "Thông báo",1);
                }
                else
                     JOptionPane.showMessageDialog(this, "Mã loại SP này đã tồn tại, không thể thêm", "Thông báo", 2);
                    
            }
    }//GEN-LAST:event_btn_ThemLSPActionPerformed

    private void btn_XoaLSPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_XoaLSPActionPerformed
        // TODO add your handling code here:
        if(tbl_LoaiSP.getSelectedRowCount()==1)
        {
            if(JOptionPane.showConfirmDialog(this, "Bạn có chắc muốn xóa không?", "Thông báo", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION){
                String ma = txt_MaLSP.getText().trim();
                String sql = "delete from LoaiSanPham where MaLSP = '"+ma+"'";
                if(LoaiSPDAO.ThemXoaSua(sql)!=-1)
                {
                     dslsp = LoaiSPDAO.getDSLSP();
                    laydulieuLSP(dslsp);
                    JOptionPane.showMessageDialog(this, "Xóa thành công !", "Thông báo", 1);
                }
                else
                    JOptionPane.showMessageDialog(this, "Dữ liệu này đang được sử dụng, không thể xóa", "Thông báo", 2);
                    
            }
        }else{
            if(tbl_LoaiSP.getRowCount()==0)
                JOptionPane.showMessageDialog(this, "Bảng trống","Thông báo",2);
            else
                JOptionPane.showMessageDialog(this, "Chưa chọn dòng cần xóa","Thông báo",2);
        }
    }//GEN-LAST:event_btn_XoaLSPActionPerformed

    private void btn_SuaLSPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_SuaLSPActionPerformed
        // TODO add your handling code here:
        if(tbl_LoaiSP.getSelectedRowCount()==1)
        {
            if(JOptionPane.showConfirmDialog(this, "Bạn có chắc muốn sửa không?", "Thông báo", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION){
                    String ma = txt_MaLSP.getText().trim();
                    String ten = txt_TenLSP.getText().trim();
                    String sql = "update LoaiSanPham set TenLSP = N'"+ten+"' where MaLSP = '"+ma+"'";
                    LoaiSPDAO.ThemXoaSua(sql);
                    dslsp = LoaiSPDAO.getDSLSP();
                    laydulieuLSP(dslsp);
                    JOptionPane.showMessageDialog(this, "Sửa thành công !", "Thông báo", 1);
            }
        }else{
            if(tbl_LoaiSP.getRowCount()==0)
                JOptionPane.showMessageDialog(this, "Bảng trống","Thông báo",2);
            else
                JOptionPane.showMessageDialog(this, "Chưa chọn dòng cần sửa","Thông báo",2);
        }
    }//GEN-LAST:event_btn_SuaLSPActionPerformed

