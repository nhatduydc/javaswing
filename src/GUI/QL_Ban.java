    public void laydulieuban(ArrayList<Ban> dsb){
        tblData.removeAllElements();
        for(Ban b : dsb){
            Vector v = new Vector();
            v.add(b.getMaban()); v.add(b.getTenban()); 
            v.add(b.getTrangthai()); 
            tblData.add(v);
        }
        tbl_Ban.setModel(new DefaultTableModel(tblData, tblTitle));
    }
