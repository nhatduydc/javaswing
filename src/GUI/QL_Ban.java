    private void btn_TimTenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_TimTenActionPerformed
        // TODO add your handling code here:
        String ten = txt_TenBan.getText().trim().toLowerCase();
        DefaultTableModel tableModel = (DefaultTableModel)tbl_Ban.getModel();
        TableRowSorter<DefaultTableModel> tr = new TableRowSorter<DefaultTableModel>(tableModel);
        tbl_Ban.setRowSorter(tr);
        tr.setRowFilter(RowFilter.regexFilter(ten));
    }//GEN-LAST:event_btn_TimTenActionPerformed

